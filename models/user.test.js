/************************************** apply */

describe("apply", function () {
// Creates application for existing user
  test("works", async function () {
    await User.apply("u2", 3);
    const found = await db.query("SELECT * FROM applications WHERE username = 'u2'");
    expect(found.rows.length).toEqual(2);
  });
// Throws error if user is not found
  test("not found user", async function () {
    try {
      await User.apply('u', 2);
      fail();
    } catch (err) {
      expect(err instanceof NotFoundError).toBeTruthy();
    }
  });
// Throws error if job not found
  test("not found job", async function () {
    try {
      await User.apply('u1', 0);
      fail();
    } catch (err) {
      expect(err instanceof NotFoundError).toBeTruthy();
    }
  });
// Throws error if user has already applied (user + job)
  test("bad request with duplicate application", async function () {
    try {
      await User.apply('u1', 2);
      fail();
    } catch (err) {
      expect(err instanceof BadRequestError).toBeTruthy();
    }
  });
});